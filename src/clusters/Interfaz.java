    public Double[][] getDatosTabla1(JTable jt) {
        TableModel tm = jt.getModel();
        int nfil = tm.getRowCount();
        int ncol = tm.getColumnCount();
        Double[][] dat = new Double[nfil][ncol];
        for (int i = 0; i < nfil; i++) {
            for (int j = 0; j < ncol; j++) {
                dat[i][j] = Double.parseDouble(String.valueOf(tm.getValueAt(i, j)));
            }
        }
        return dat;
    }

    public TableModel arre2ToTM(Double[][] arre) {
        int col = 0, fil = 0;
        Vector cnames = new Vector();
        Vector datos = new Vector();
        TableModel tm;
        fil = arre.length;
        col = arre[0].length;
        for (int i = 0; i < col; i++) {
            cnames.addElement("C" + i);
        }
        for (int i = 0; i < fil; i++) {
            Vector aux = new Vector();
            for (int j = 0; j < col; j++) {
                aux.addElement(dec4.format(arre[i][j]));
            }
            datos.addElement(aux);
        }
        tm = new DefaultTableModel(datos, cnames);
        return tm;
    }
    
