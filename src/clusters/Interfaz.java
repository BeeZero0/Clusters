    public String[] colnames() {
        String[] coln = new String[coordenadas];
        for (int i = 0; i < coordenadas; i++) {
            if (i == 0) {
                coln[i] = "X";
            } else {
                coln[i] = "Y";
            }
        }
        return coln;
    }

